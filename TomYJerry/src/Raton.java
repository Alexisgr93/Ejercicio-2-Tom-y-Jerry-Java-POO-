
public class Raton {
	
	private double peso;
	private double posicion;

	public double getPeso() {
		return peso;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}
	
	
	public double VelocidaddeJerry() {
		
		return 10 - this.peso;
		
	}

	public double getPosicion() {
		return posicion;
	}

	public void setPosicion(double posicion) {
		this.posicion = posicion;
	}

}
